// tipagem dinâmica
// Untyped: não precisamos declarar o tipo da variável

let minhaVar = 567;
console.log("Declarando números: " + minhaVar);
console.log("Tipo da variável: " + typeof minhaVar);
console.log();

minhaVar = "texto";
console.log("Declarando strings: " + minhaVar);
console.log("Tipo da variável: " + typeof minhaVar);
console.log();

minhaVar = false;
console.log("Declarando boolean: " + minhaVar);
console.log("Tipo da variável: " + typeof minhaVar);
console.log();
